def pprint(step, loss=None, accuracy=None):
    format_str = '%s: step %d, '
    if loss:
        format_str += 'loss = %.2f'
        print (format_str % (datetime.now(), step, loss))
    if accuracy:
        format_str += 'accuracy = %.2f'
        print (format_str % (datetime.now(), step, accuracy))

